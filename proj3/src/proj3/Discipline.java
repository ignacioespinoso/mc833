package proj3;

public class Discipline {
    public String codigo;
    public String nome;
    public String ementa;
    public String professor;
    public String sala;
    public String comentario;

    public Discipline(String codigo, String nome, String ementa, String professor, String sala, String comentario) {
        this.codigo = codigo;
        this.nome = nome;
        this.ementa = ementa;
        this.professor = professor;
        this.sala = sala;
        this.comentario = comentario;
    }
}
